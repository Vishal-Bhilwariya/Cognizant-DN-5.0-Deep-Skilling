/*

    # What are Design Principles? 
    - Design Principles are general guidelines or best practices that help developers write clean, maintainable, reusable, and scalable code.
      They tell us : "How should we design our code?"
      Examples: SOLID Principles

    # What are Design Patterns? 
    - Design Patterns are proven, reusable solutions to common software design problems.
      They tell us: "How can we solve a specific design problem?"
      Examples: Singleton Pattern , Factory Pattern

    # Why Do We Need Them? 
    - Imagine you're building an E-Commerce Website.
       Problems you may face:
            Multiple payment methods
            Multiple users
            Multiple notifications
            Multiple databases
        If every developer writes their own solution,the code becomes inconsistent.

        Design Principles and Design Patterns provide standard ways to design software.
            Problem
                ↓
            Design Principle
                ↓
            Design Pattern
                ↓
            Clean Solution

                Software Development
                       |
        ---------------------------------
        |                               |
 Design Principles              Design Patterns
        |                               |
   Rules & Guidelines         Ready-made Solutions
        |                               |
     SOLID                     Factory, Singleton


    # Why Both Are Needed?
        Without Design Principles:
            Poor code quality
            Tight coupling
            Difficult maintenance

        Without Design Patterns:
            Developers reinvent the same solution repeatedly
            Inconsistent implementations
            Increased development time

        Together they produce:
            Good Principles + Good Patterns = Good Software Design
*/