/*

    # Overview of Spring Boot :
    - Spring Boot is an extension of the Spring Framework that makes it easier and faster to build Spring applications with minimum configuration.

    Earlier, using only Spring Framework, developers had to:
        Configure XML files
        Configure DispatcherServlet
        Configure Tomcat
        Configure Beans
        Add many dependencies manually

        This required a lot of boilerplate configuration.
        Spring Boot solves this problem.
        It automatically configures many things, allowing developers to focus mainly on business logic.

    Real-Life Analogy
     Imagine buying a new smartphone.
     Traditional Spring
     You buy a phone.
        Then you install:
            Operating System
            Apps
            Drivers
            Settings
        Everything manually.

     Spring Boot
        You buy a phone.
        Everything is already installed.
        You simply switch it on and start using it.

    # Features of Spring Boot :
    1. Easy Setup - Very little configuration is required.
    2. Embedded Server - No need to install Tomcat separately.
            Embedded servers include:
                Tomcat (Default)
                Jetty
                Undertow
    3. Starter Dependencies - Instead of adding many individual libraries,
        Spring Boot provides Starters.
            Example: spring-boot-starter-web
                It automatically includes the required web-related libraries.

    4. Production Ready -Spring Boot provides production-ready features such as:
        Health checks
        Monitoring
        Metrics
    
    5. Rapid Development - Applications can be developed much faster compared to traditional Spring.

    Advantages
         Easy to learn.
         Minimal configuration.
         Faster development.
         Embedded web server.
         Starter dependencies.
         Production-ready features.
         Reduced boilerplate code.
*/